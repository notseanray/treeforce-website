						TreeForce is proud to present what we have been developing for the past few months: our
						Hydroponics at Home system. This is a fully functional hydroponics system, designed
						specifically to be used in classrooms in order to teach students essential agriculture
						and computer science skills. We firmly believe that instilling these skills in younger
						students is critical if we want to protect the future of agriculture and our
						environment, and the Hydroponics at Home system does just that.
					</div>
					<div class="text-xl pb-4 text-[#c8dbbf]">
						This system features three rows of 4 pipes that can be used to grow various crops. The
						multi-layer system allows for better space management, allowing you to grow more crops
						by utilizing vertical space. In order to allow for more efficient light management, the
						system features a set of grow-lights under each level to provide an equivalent amount of
						light to the various levels. This also allows for the system to work during times where
						there is less sunlight, or in darker areas, such as inside classrooms. We also designed
						this system with mobility in mind, and the entire system is mobile thanks to the wheels
						attached on the bottom of the system. This ensures that it is possible for the system to
						move easily, something that has proved invaluable in our testing. The system operates
						using an active pump system that allows the water to constantly cycle throughout the
						system. This pump mechanism intakes water from the water storage tank, and then forces
						it to the top, where gravity will force it back to the bottom. This process will loop,
						allowing for the plants to grow.
					</div>
					<div class="text-xl pb-4 text-[#c8dbbf]">
